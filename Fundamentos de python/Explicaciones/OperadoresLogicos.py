# Operadores aritméticos:

# Suma (+): Se utiliza para sumar dos valores.
# Resta (-): Se utiliza para restar un valor de otro.
# Multiplicación (*): Se utiliza para multiplicar dos valores.
# División (/): Se utiliza para dividir un valor por otro. Devuelve un resultado de tipo float.
# División entera (//): Se utiliza para realizar una división entera, descartando el residuo. Devuelve un resultado de tipo int.
# Módulo (%): Se utiliza para obtener el residuo de una división entera.
# Exponente (**): Se utiliza para elevar un valor a una potencia.
# Operadores de asignación:

# Asignación (=): Se utiliza para asignar un valor a una variable.
# Asignación con suma (+=): Se utiliza para sumar un valor a la variable y asignar el resultado a la misma variable.
# Asignación con resta (-=): Se utiliza para restar un valor a la variable y asignar el resultado a la misma variable.
# Asignación con multiplicación (*=): Se utiliza para multiplicar un valor a la variable y asignar el resultado a la misma variable.
# Asignación con división (/=): Se utiliza para dividir un valor a la variable y asignar el resultado a la misma variable.
# Asignación con división entera (//=): Se utiliza para realizar una división entera y asignar el resultado a la misma variable.
# Asignación con módulo (%=): Se utiliza para obtener el residuo de una división entera y asignar el resultado a la misma variable.
# Asignación con exponente (**=): Se utiliza para elevar un valor a una potencia y asignar el resultado a la misma variable.
# Aquí tienes algunos ejemplos de cómo se utilizan estos operadores:


# Operadores aritméticos
a = 5 + 3  # Suma: a tendrá el valor 8
b = 10 - 2  # Resta: b tendrá el valor 8
c = 4 * 2  # Multiplicación: c tendrá el valor 8
d = 16 / 2  # División: d tendrá el valor 8.0
e = 17 // 3  # División entera: e tendrá el valor 5
f = 17 % 3  # Módulo: f tendrá el valor 2
g = 2 ** 3  # Exponente: g tendrá el valor 8

# Operadores de asignación
x = 5  # Asignación: x tendrá el valor 5
x += 3  # Asignación con suma: x tendrá el valor 8
y = 10
y -= 2  # Asignación con resta: y tendrá el valor 8
z = 4
z *= 2  # Asignación con multiplicación: z tendrá el valor 8
w = 16
w /= 2  # Asignación con división: w tendrá el valor 8.0
p = 17
p //= 3  # Asignación con división entera: p tendrá el valor 5
q = 17
q %= 3  # Asignación con módulo: q tendrá el valor 2
r = 2
r **= 3  # Asignación con exponente: r tendrá el valor 8