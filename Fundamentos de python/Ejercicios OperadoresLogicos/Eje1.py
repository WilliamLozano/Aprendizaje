# Escribe un programa que tome dos números ingresados por el usuario y muestre la suma, resta, multiplicación y división de los dos números.

num1 = int(input("Ingrese el primer numero al sistema : "))
num2 = int(input("Ingrese el segundo numero al sistema : "))

print(f"el resultado de la suma es : {num1 + num2}")
print(f"el resultado de la resta es : {num1 - num2}")
print(f"el resultado de la multiplacion es : {num1 * num2}")
print(f"el resultado de la division flotante es  : {num1 / num2}")
print(f"el resultado de la division entera es  {num1 // num2}")